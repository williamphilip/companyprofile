                        <h1>Tentang Kami</h1>
                        <p>Rumah Sakit Ibu dan Anak Catherine Booth Makassar adalah salah satu Rumah Sakit di
                            Makassar yang berdiri sejak 1931 dan merupakan salah satu Rumah Sakit Bala Keselamatan
                            di Indonesia. Rumah Sakit Ibu dan Anak Catherine Booth pada awalnya adalah Rumah Sakit
                            Misi Pelayanan, tetapi dengan berjalannya waktu, maka Rumah Sakit Ibu dan Anak
                            Catherine Booth harus mampu pula bersaing dengan rumah sakit lain dan mampu pula
                            menghadapi tantangan era globalisasi.
                        </p>
                        <p>Pada era globalisasi Rumah Sakit diharapkan dapat mengikuti perkembangan diantara
                            perkembangan teknologi dan meningkatkan pelayanan terhadap custumor (pasien) dengan
                            lebih memperhatikan nilai kepuasan.
                        </p>
                        <p>Dalam memberika pelayanan ini pun harus sesuai dengan prosedur karena pada era
                            globalisasi ini, semua tindakan dapat menimbulkan tuntutan malpraktek. Oleh karena itu
                            Rumah Sakit Ibu dan Anak Catherine Booth Makassar mempelajari kekuatan dan kelemahan
                            serta tantangan dan peluang yang ada untuk mencapai sasaran meningkatkan kunjungan dan
                            BOR rumah sakit, maka Rumah Sakit Ibu dan Anak Catherine Booth Makassar harus membuat
                            renstra yang tetap berfokus kepada Visi dan Misi Bala Keselamatan dan Yayasan Pelayanan
                            Kesehatan Bala Keselamatan (YPKBK).
                        </p>
